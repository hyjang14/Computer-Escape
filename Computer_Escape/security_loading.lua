local composer = require( "composer" )
local scene = composer.newScene()

function scene:create( event )
	local sceneGroup = self.view

	-- ↓ 배경 ----------------------------------------------------------------------------------------------------
	local background = display.newImageRect("image/컴퓨터화면.png", display.contentWidth, display.contentHeight)
	background.x, background.y = display.contentWidth/2, display.contentHeight/2
	sceneGroup:insert(background)

	-- ↓ 시간 -------------------------------------------------------------------------------------------------

	local hour = os.date( "%I" )
	local minute = os.date( "%M" )

	local hourText = display.newText(hour, display.contentWidth*0.919, display.contentHeight*0.972, font_Speaker)
	hourText.size = 46
	hourText:setFillColor(0)
	local minuteText = display.newText(minute, display.contentWidth*0.975, display.contentHeight*0.972, font_Speaker)
	minuteText.size = 46
	minuteText:setFillColor(0)

	local text = display.newText(":", display.contentWidth*0.946, display.contentHeight*0.96, font_Speaker)
    text.size = 100
    text:setFillColor(0)
	sceneGroup:insert(text)

	local function counter( event )
		hour = os.date( "%I" )
		hourText.text = hour
		minute = os.date( "%M" )
		minuteText.text = minute
	end

	timer.performWithDelay(1000, counter, -1)

	sceneGroup:insert(hourText)
	sceneGroup:insert(minuteText)

	-- ↑ 시간 -------------------------------------------------------------------------------------------------

	-- ↓ 보안창 ----------------------------------------------------------------------------------------------------------------------------
	local background = display.newImage("image/보안/1.시스템보안.png")
	background.x, background.y = display.contentCenterX, display.contentHeight * 0.5

	local loading = display.newImageRect("image/보안/초록로딩.png", 148.4, 85)
	loading.x, loading.y = display.contentWidth * 0.307, display.contentHeight * 0.718
	loading.anchorX = -1

	sceneGroup:insert(background)
	sceneGroup:insert(loading)

	-- ↑ 오브젝트 정리 ---------------------------------------------------------------------------------------------------------------------

	-- ↓ 함수 정리 -------------------------------------------------------------------------------------------

	local go = 5
	local function counter( event )
		if go > 1 then
			loading.width = (7 - go) * 148.4
		end
		go = go - 1
		if(go == -1) then
			composer.showOverlay( "security_game1" )
		end

	end

 	timer.performWithDelay(1000, counter, 6)

	
end

